function setup() {
  createCanvas(1440,1080)
  //creates a grey canvas that is 1440x1080 pixles
}

function draw() {
  ellipse(100,100,100,100)//creates a black ellipse at coordinates (100,100) with width of 100 and hieght of 100
  fill(255,204,0) //changes the fill of each shape to black
  rect(200,200,200,200)//creates a black rectangle at coordinates (200,200) with width of 200 and hieght of 200
  
}